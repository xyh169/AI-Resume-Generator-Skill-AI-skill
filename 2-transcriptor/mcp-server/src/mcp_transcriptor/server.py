from mcp.server.fastmcp import FastMCP
import httpx
import json

mcp = FastMCP("Resume Transcriptor", description="简历降噪、重构与内容提取专家 MCP 服务")

@mcp.prompt()
def resume_rewrite_guidelines(has_jd: bool = False, keyword: str = "") -> str:
    """获取专业的简历重写与结构化降噪指南。 Agent 在处理散漫简历时必须阅读并应用这些规则。
    
    Args:
        has_jd: 如果上下文中用户已经提供了明确的 JD (职位描述)，请传 True，反之传 False。
        keyword: 如果用户指明了特定公司或岗位，请当做 keyword 传入进行参考要求。
    """
    
    base = """
# 简历降噪与专业化重构铁律

你是一个专业的猎头与文档架构师，必须对用户提供的漫谈式简历执行以下动作，输出极其标准格式的 Markdown 文件。

## Step 1: 降噪、清洗与语言专业化
所有的口语内容必须在这个阶段被转化为具有高商业价值的职业化话术。

- **不能捏造**：只能做减法（去除无用废话、情绪表达）和重构（整理从句、提炼逻辑），绝不替用户捏造原本不存在的数据与成果。
- **摒弃弱势口语**：
  - ❌ “我弄出来了”、“我大概从 x 年都在干这个”
  - ✅ “主导研发”、“独立负责”、“全流程跟进”
  - ❌ “我是领头人，带头带着3个组一起办了这事”
  - ✅ “作为项目负责人，跨部门统筹 3 个团队，推动项目落地与资源协调”
- **信息抽屉法**：将荣誉丢进荣誉板块，专利丢进专利板块，不要混杂在常规项目里。
- **STAR 法则补齐**：『情境背景 + 核心动作 + 定量/定性结果』（例如：整合X与Y实现Z的全自动检测，指标提升至W）。

## Step 2: 格式要求
请仔细对齐要求的排版模块（如：头衔栏、教育背景、工作/实习经历、项目经历、成果与荣誉），通过加粗关键指标 `**xxx**` 进行高亮。
禁止输出无意义的换行符或多个空行。保持页面紧凑。写文件前请去除残留 `\n\n\n`。
"""

    if has_jd:
        base += "\n\n💡 **[定制化强化要求]**\n用户已提供了目标求职岗位的 JD。请你在重塑项目经历时，重火力**提炼并利用加粗**与该岗位核心要求最匹配的工具、业务场景或能力。边缘经历可极度弱化！"
    elif keyword:
        base += f"\n\n💡 **[定制化强化要求]**\n用户想针对【{keyword}】的岗位投递。请先调用搜索工具（如 search_web 等）确认该方向的招聘核心要求，并在之后改写简历时针对性优化用词！"

    return base

@mcp.tool()
def search_job_description(company_or_title: str) -> str:
    """搜索目标公司/职位的招聘要求与偏好关键词 (由于暂未接入真实招聘接口，这里仅作为一个 Agent 占位引导，Agent也可以自发用浏览器执行搜索)。"""
    return f"为了优化简历重写，建议使用你的内置 search 搜索工具或浏览器去检索: '{company_or_title} 岗位职责 招聘要求'，以获取最新的用人画像。"

def main():
    mcp.run()

if __name__ == "__main__":
    main()
